import { Schema, model } from "mongoose";

const adminUser = Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Admin = model("admin", adminUser);

export default Admin;
